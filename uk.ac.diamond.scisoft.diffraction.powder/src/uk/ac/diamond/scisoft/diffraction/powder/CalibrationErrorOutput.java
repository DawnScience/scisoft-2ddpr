package uk.ac.diamond.scisoft.diffraction.powder;

public class CalibrationErrorOutput {

	private Double wavelength;
	private Double beamCentreX;
	private Double beamCentreY;
	private Double tilt;
	private Double tiltAngle;
	private Double distance;
	
	public Double getWavelength() {
		return wavelength;
	}
	
	public void setWavelength(Double wavelength) {
		this.wavelength = wavelength;
	}
	
	public Double getBeamCentreX() {
		return beamCentreX;
	}
	
	public void setBeamCentreX(Double beamCentreX) {
		this.beamCentreX = beamCentreX;
	}
	
	public Double getBeamCentreY() {
		return beamCentreY;
	}
	
	public void setBeamCentreY(Double beamCentreY) {
		this.beamCentreY = beamCentreY;
	}
	
	public Double getTilt() {
		return tilt;
	}
	
	public void setTilt(Double tilt) {
		this.tilt = tilt;
	}
	
	public Double getTiltAngle() {
		return tiltAngle;
	}
	
	public void setTiltAngle(Double tiltAngle) {
		this.tiltAngle = tiltAngle;
	}
	
	public Double getDistance() {
		return distance;
	}
	
	public void setDistance(Double distance) {
		this.distance = distance;
	}
	
}
